-- Например - разделить 13 участников на 4 равные команды

SELECT i, NTILE(4) OVER()
FROM generate_series(1, 13) i;

--  i  | ntile 
-- ----+-------
--   1 |     1
--   2 |     1
--   3 |     1
--   4 |     1
--   5 |     2
--   6 |     2
--   7 |     2
--   8 |     3
--   9 |     3
--  10 |     3
--  11 |     4
--  12 |     4
--  13 |     4
